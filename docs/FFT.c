void fft(float xr[], float xi[], short NN, short M)
{
  short L, B, J, P, k, GG, KK;
  float rPartKB, iPartKB;

  ChangeOrder(xr, xi, NN);

  for(L = 1; L <= 10; L++)
  { 
    B  = mypowdataB[L - 1];
    GG = mypowdataGG[L - 1];
    KK = mypowdataKK[L - 1];
		
    for(J = 0; J <= B - 1; J++)
    {
      P = J * KK;
			
      for(k = J; k <= NN - 1;k += GG)
      {
        rPartKB = xr[k+B] * rCf[P] - xi[k+B] * iCf[P];
        iPartKB = xi[k+B] * rCf[P] + xr[k+B] * iCf[P];
        xr[k+B] = xr[k] - rPartKB;
        xi[k+B] = xi[k] - iPartKB;
        xr[k] = xr[k] + rPartKB;
        xi[k] = xi[k] + iPartKB;
      }
			
    }
  }
}

/*********************************************************
Description:  
Input:  No                               
Output: No      
Return: No 
Others: No     
**********************************************************/
void ChangeOrder(float xr[], float xi[], short NN)
{
  unsigned short LH, N1, I, J, K;
  float T;
	
  LH = NN / 2;J = LH; N1 = NN - 2;
	
  for(I = 1;I <= N1; I++)
  {
    if(I < J)
    {
      T = xr[I]; xr[I] = xr[J]; xr[J] = T;
      T = xi[I]; xi[I] = xi[J]; xi[J] = T;
    }
		
    K = LH;
		
    while(J >= K)
    {
      J = J - K;
      K=(unsigned short)(K / 2 + 0.5);
    }
		
    J = J + K;
  }
}


